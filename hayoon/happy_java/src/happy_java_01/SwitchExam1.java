package happy_java_01;

public class SwitchExam1 {
    public static void main(String[] args) {
        int num = 4;
        switch (num) {
            case 1 :
                System.out.println("1입니다.");
                break;
            case 2 :
                System.out.println("2입니다.");
                break;
            case 3 :
                System.out.println("3입니다.");
                break;
            default:
                System.out.println("1,2,3이 아닙니다.");
        }
    }
}
